export function getColor(res) {
    switch (res.name) {
        case "Habibs":
            return "#ee1a26"
        case "Tadashii":
            return "#151515"
        case "Boteco de Portal":
            return "#000"
        case "Chun-Li":
            return "#727378"
        default:
            return "#fff"
    }
}
